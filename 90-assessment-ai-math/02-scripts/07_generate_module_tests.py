def render_answer_key(variant: int, modules: list[dict[str, Any]], traceability: list[dict[str, Any]]) -> str:
    lines: list[str] = [
        f"# Вариант {variant}. Ключи ответов (для преподавателя)",
        "",
        "---",
        "",
    ]

    trace = [x for x in traceability if x["variant"] == variant]
    q_by_id = {x["question_id"]: x for x in trace}

    q_counter = 1
    for module in modules:
        lines.extend(["", "---", "", f"## {render_section_title(module)}", "", "---", ""])

        for local_idx in range(5):
            if local_idx > 0:
                lines.extend(["", "---", ""])

            qid = f"Q{q_counter}"
            q = q_by_id[qid]

            lines.append(f"### {qid} - {q['title']}")
            lines.append("")
            lines.append("---")
            lines.append("")
            lines.append(f"Тип: {qtype_label(str(q['question_type']))}")
            lines.append("")
            lines.append("Контекст:")
            lines.append(str(q["context"]))
            lines.append("")
            lines.append("Задание:")
            lines.append(str(q["task"]))
            lines.append("")

            if q["question_type"] in {"single", "multiple"}:
                lines.append("Инструкция:")
                lines.append(qtype_choice_instruction(str(q["question_type"])))
                lines.append("")
            else:
                lines.append("Подсказка (ход рассуждения):")
                for idx, step in enumerate(q.get("hint_steps", []), start=1):
                    lines.append(f"{idx}. {step}")
                lines.append("")

            if q["question_type"] == "single":
                idx = int(q["correct"][0]) + 1
                option = q["options"][idx - 1]
                lines.append(f"Правильный ответ: {idx}. {option}")
            elif q["question_type"] == "multiple":
                answer_nums = [int(i) + 1 for i in q["correct"]]
                lines.append("Правильные ответы: " + ", ".join(str(x) for x in answer_nums))
                for num in answer_nums:
                    lines.append(f"- {num}. {q['options'][num - 1]}")
            else:
                lines.append("Эталонный ответ:")
                lines.append(str(q["reference"]))
                lines.append("")
                lines.append("Критерии оценивания:")
                for idx, criterion in enumerate(q["criteria"], start=1):
                    lines.append(f"{idx}. {criterion}")

            lines.append("")
            lines.append("Технический блок:")
            lines.append(f"- Трассируемость: {q['topic_id']} -> {q['material_id']} -> {q['module_slug']} -> {qid}")
            lines.append(f"- Источник материала: {q['source_path']}")
            lines.append(f"- Уровень риска: {q['risk_level']}")
            lines.append(f"- Требуется LaTeX: {'да' if q['requires_latex'] else 'нет'}")
            lines.append(f"- Профиль стиля: {q.get('style_profile', STYLE_PROFILE)}")
            lines.append(f"- Уровень сложности: {q['difficulty_level']}")
            lines.append(f"- Область знаний: {q['knowledge_scope']}")
            q_counter += 1

    return "\n".join(lines).rstrip() + "\n"


