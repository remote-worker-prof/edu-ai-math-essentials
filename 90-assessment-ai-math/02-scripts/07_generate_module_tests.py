def log(message: str) -> None:
    print(f"[07_generate][INFO] {message}", flush=True)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate module-based assessment variants.")
    parser.add_argument("--project-root", default=".", help="Project root directory.")
    parser.add_argument("--workspace-root", default="90-assessment-ai-math", help="Assessment workspace root.")
    parser.add_argument("--seed", type=int, default=SEED, help="Deterministic seed value.")
    return parser.parse_args()


def select_items(items: list[dict[str, Any]], count: int, variant: int) -> list[dict[str, Any]]:
    if not items:
        return []
    offset = (variant - 1) * count
    return [items[(offset + i) % len(items)] for i in range(count)]


def build_section_questions(topic_id: str, variant: int) -> list[dict[str, Any]]:
    bank = QUESTION_BANK.get(topic_id)
    if not bank:
        return []

    singles = select_items(bank["single"], 2, variant)
    multiples = select_items(bank["multiple"], 2, variant)
    opens = select_items(bank["open"], 1, variant)

    questions: list[dict[str, Any]] = []
    for item in singles:
        questions.append({**item, "qtype": "single"})
    for item in multiples:
        questions.append({**item, "qtype": "multiple"})
    for item in opens:
        questions.append({**item, "qtype": "open"})
    return questions


def render_section_title(module: dict[str, Any]) -> str:
    topic_id = str(module.get("topic_id", ""))
    return SECTION_TITLES_RU.get(topic_id, str(module.get("module_title", "Раздел")))


def qtype_label(qtype: str) -> str:
    if qtype == "single":
        return "один вариант ответа."
    if qtype == "multiple":
        return "несколько вариантов ответа."
    return "открытый вопрос."


def qtype_choice_instruction(qtype: str) -> str:
    if qtype == "single":
        return "Выберите один вариант ответа."
    if qtype == "multiple":
        return "Выберите все верные варианты ответа."
    return ""


def render_test_variant(variant: int, modules: list[dict[str, Any]], traceability: list[dict[str, Any]]) -> str:
    lines: list[str] = [
        f"# Вариант {variant}. Тест по дисциплине «Математические основы ИИ»",
        "",
        "---",
        "",
        "Инструкция:",
        "1. Для закрытых вопросов укажите номер или набор номеров выбранных вариантов.",
        "2. Для открытых вопросов дайте развернутый аргументированный ответ.",
        "",
    ]

    q_counter = 1
    for sec_idx, module in enumerate(modules):
        lines.extend(["", "---", "", f"## {render_section_title(module)}", "", "---", ""])

        questions = build_section_questions(str(module["topic_id"]), variant)
        for local_idx, q in enumerate(questions):
            if local_idx > 0:
                lines.extend(["", "---", ""])
            lines.append(f"### Q{q_counter} - {q['title']}")
            lines.append("")
            lines.append("---")
            lines.append("")
            lines.append(f"Тип: {qtype_label(q['qtype'])}")
            lines.append("")
            lines.append("Формулировка вопроса:")
            lines.append(str(q["prompt"]))
            lines.append("")

            if q["qtype"] in {"single", "multiple"}:
                lines.append(qtype_choice_instruction(q["qtype"]))
                lines.append("")
                lines.append("Варианты ответа:")
                lines.append("")
                for idx, option in enumerate(q["options"], start=1):
                    lines.append(f"{idx}. {option}")
            else:
                lines.append("Ответ представьте в развернутой форме (3-5 предложений).")

            material_pool = module.get("materials", [])
            if material_pool:
                material = material_pool[(local_idx + variant - 1) % len(material_pool)]
                material_id = material.get("material_id", "n/a")
                source_path = material.get("source_path", "n/a")
            else:
                material_id = "n/a"
                source_path = "n/a"

            traceability.append(
                {
                    "variant": variant,
                    "question_id": f"Q{q_counter}",
                    "question_type": q["qtype"],
                    "topic_id": module["topic_id"],
                    "module_slug": module["module_slug"],
                    "section_title": render_section_title(module),
                    "material_id": material_id,
                    "source_path": source_path,
                    "title": q["title"],
                    "prompt": q["prompt"],
                    "correct": q.get("correct", []),
                    "reference": q.get("reference", ""),
                    "criteria": q.get("criteria", []),
                    "options": q.get("options", []),
                }
            )

            q_counter += 1

    return "\n".join(lines).rstrip() + "\n"


